              cv::eigen2cv(frame_PnP_Rs, pnp_rotation);
		      cv::eigen2cv(frame_PnP_Ps, pnp_trans);
		  
		      Eigen::Vector3d t_imu = Ps[WINDOW_SIZE] + Rs[WINDOW_SIZE] * tic[0];
              Eigen::Matrix3d R_imu = Rs[WINDOW_SIZE] * ric[0];
		  
		      cv::eigen2cv(R_imu, preint_rotation);
		      cv::eigen2cv(t_imu, preint_trans);
		      //calculate reporjection errors 		  
		      if(!reproject_points.empty())
		  	    reproject_points.clear();
		  
		     //reprojection errors of PnP results
