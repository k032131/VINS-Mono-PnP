		  double pnp_reproject_error = 0.0;
		  double preint_reproject_error = 0.0;
		  if(!pts_3_vector.empty())
		  	pts_3_vector.clear();
		  if(!pts_2_vector.empty())
		  	pts_2_vector.clear();
		  //注意此处solvePnP得到的变换矩阵为T_w^c，而Ps指的是imu到世界坐标系中的变换R_imu^w
		  inImagePoints(pts_3_vector, pts_2_vector);
		  f_manager.removeOutlier();
