//利用PnP求解相对位姿所用
bool Estimator::framePnP(vector<cv::Point3f>& map_points, vector<cv::Point2f>& image_points, Matrix3d &R_matrix, Vector3d &P_vector)//, int before_frame_num, int cur_frame_num)
{
	if (int(image_points.size()) < 6)
	{
		printf("Too little tracked points used for framePnP\n");
		//if (int(pts_2_vector.size()) < 10)
			return false;
	}

	cv::Mat r, rvec, t, tmp_r;
	cv::eigen2cv(R_matrix, tmp_r);
	cv::Rodrigues(tmp_r, rvec);
	cv::eigen2cv(P_vector, t);
	
	bool pnp_succ;
	pnp_succ = cv::solvePnPRansac(map_points, image_points, K, D, rvec, t);
	if(!pnp_succ)
	{
	    cout << "solve PnP failed!"<< endl;
		return false;
	}
	cv::Rodrigues(rvec, r);
	//cout << "r " << endl << r << endl;
	MatrixXd R_pnp;
	cv::cv2eigen(r, R_pnp);
	MatrixXd T_pnp;
	cv::cv2eigen(t, T_pnp);
	R_matrix = R_pnp;
	P_vector = T_pnp;
	return true;

}

